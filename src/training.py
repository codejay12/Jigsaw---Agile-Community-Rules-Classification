from datasets import Dataset

def convert_to_alpaca_format(df):
    instructions = []
    inputs = []
    outputs = []

    for _, row in df.iterrows():
        instruction = "Classify if the given Reddit comment violates the given rule. Only answer Yes or No."

        input_text = f"""r/{row.subreddit}
Rule: {row.rule}

1) {row.positive_example_1}
Violation: Yes

2) {row.negative_example_1}
Violation: No

3) {row.negative_example_2}
Violation: No

4) {row.positive_example_2}
Violation: Yes

5) {row.body}"""

        # You need to define the output for your current comment (label)
        # Let's assume you already have a `label` column as 'Yes' or 'No'
        output_text = row["label"]  # e.g., "Yes" or "No"

        instructions.append(instruction)
        inputs.append(input_text)
        outputs.append(output_text)

    # Create Hugging Face Dataset
    dataset_dict = {
        "instruction": instructions,
        "input": inputs,
        "output": outputs,
    }

    return Dataset.from_dict(dataset_dict)

# Convert
alpaca_dataset = convert_to_alpaca_format(df)
EOS_TOKEN = tokenizer.eos_token
alpaca_prompt = """
You are given a comment on reddit. Your task is to classify if it violates the given rule. Only respond Yes/No.
"""

def formatting_prompts_func(examples):
    instructions = examples["instruction"]
    inputs       = examples["input"]
    outputs      = examples["output"]
    texts = []
    for instruction, input, output in zip(instructions, inputs, outputs):
        text = alpaca_prompt.format(instruction, input, output) + EOS_TOKEN
        texts.append(text)
    return { "text" : texts }

# Apply formatting
dataset = alpaca_dataset.map(formatting_prompts_func, batched=True)

from trl import SFTTrainer
from transformers import TrainingArguments
from unsloth import is_bfloat16_supported

trainer = SFTTrainer(
    model = model,
    tokenizer = tokenizer,
    train_dataset = dataset,
    dataset_text_field = "text",
    max_seq_length = max_seq_length,
    dataset_num_proc = 2,
    packing = False, # Can make training 5x faster for short sequences.
    args = TrainingArguments(
        per_device_train_batch_size = 2,
        gradient_accumulation_steps = 4,
        warmup_steps = 5,
        # num_train_epochs = 1, # Set this for 1 full training run.
        max_steps = 60,
        learning_rate = 2e-4,
        fp16 = not is_bfloat16_supported(),
        bf16 = is_bfloat16_supported(),
        logging_steps = 1,
        optim = "adamw_8bit",
        weight_decay = 0.01,
        lr_scheduler_type = "linear",
        seed = 3407,
        output_dir = "outputs",
        report_to = "none", # Use this for WandB etc
    ),
)

trainer_stats = trainer.train()

